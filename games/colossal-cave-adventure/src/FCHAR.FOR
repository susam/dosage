      INTEGER*4 FUNCTION FCHAR(LINE)
C
C  FINDS THE POSITION OF THE FIRST NON-BLANK CHARACTER IN A CHARACTER STRING.
C  IF THE STRING IS BLANK, A VALUE OF 1 IS RETURNED.
C
      CHARACTER LINE * (*)
      
      FCHAR=1
      DO 1 I=1,LEN(LINE)
      IF(LINE(I:I).NE.' ') THEN
         FCHAR=I
         GOTO 100
      ENDIF
    1 CONTINUE
  100 RETURN
      END
