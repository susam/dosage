      INTEGER*4 FUNCTION LCHAR(LINE)
C
C  FINDS THE POSITION OF THE LAST NON-BLANK CHARACTER IN A CHARACTER STRING.
C  IF THE STRING IS BLANK, A VALUE OF 1 IS RETURNED.
C
      CHARACTER LINE * (*)
      
      LCHAR=1
      DO 1 I=LEN(LINE),1,-1
      IF(LINE(I:I).NE.' ') THEN
         LCHAR=I
         GOTO 100
      ENDIF
    1 CONTINUE
  100 RETURN
      END
      
